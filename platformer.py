blocklist = []

for y in range(0,len(level1)):
    for x in range(0,len(level1[y])):
        if (level1[y][x]==1):
            blocklist.append(Block(x*32,y*32))


class Screen:
    windowWidth = 800
    windowHeight = 600
    screenColor = (50,60,200)
    clock = pygame.time.Clock()
    gravity=-1

    def __init__(self):
        self.running = True
        self.display_window= None
        self.player = Player(0,400)
        self.level = Block(50,50)

    def on_init(self):
        pygame.init()
        self.display_window= pygame.display.set_mode((self.windowWidth, self.windowHeight), pygame.HWSURFACE)
        self.player.loadPlayerImage()
        pygame.display.set_caption("Platformer")
        self.running = True

        while self.running:
            for event in pygame.event.get():
                if (event.type == pygame.QUIT):
                    self.running=False

            self.display_window.fill(self.screenColor)

            for block in blocklist:
                block.render(self.display_window)

            keys = pygame.key.get_pressed()

            if (keys[K_RIGHT]):
                self.player.moveRight()

            if (keys[K_LEFT]):
                self.player.moveLeft()

            if (keys[K_UP]):
                self.player.jump()

            self.player.update(self.gravity, blocklist)
            self.player.render(self.display_window)
            pygame.display.flip()
        pygame.quit()


